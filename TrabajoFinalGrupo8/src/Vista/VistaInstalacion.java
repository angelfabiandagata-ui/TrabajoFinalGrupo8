    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int filaSelec = jTable1.getSelectedRow();
        try {
            if (filaSelec == -1) {
                JOptionPane.showMessageDialog(this,"Debe elegir una instalacion de la tabla para dar de baja", "Requierido" ,JOptionPane.WARNING_MESSAGE);
                return;
            }
            Object valorCod = modeloTabla.getValueAt(filaSelec, 0);

            int codigoInstalacion = (int)valorCod;

            if (checkAlta.isSelected()) {
                instalacionData.altaLogica(codigoInstalacion);
                JOptionPane.showMessageDialog(this, "Instalacion dada de ALTA correctamente.", "Estado Cambiado", JOptionPane.INFORMATION_MESSAGE);

            } else if (checkBaja.isSelected()) {
                int confirmacion = JOptionPane.showConfirmDialog(this,
                    "¿Está seguro de que desea dar de BAJA LÓGICA a la instalacion con Codigo: " + codigoInstalacion + "?",
                    "Confirmar Baja", JOptionPane.YES_NO_OPTION);

                if (confirmacion == JOptionPane.YES_OPTION) {
                    instalacionData.bajaLogica(codigoInstalacion);
                    JOptionPane.showMessageDialog(this, "Instalacion dada de BAJA correctamente.", "Estado Cambiado", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar Alta o Baja.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            actulizarTabla();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe cargar el Codigo de Instalacion en el primer campo para cambiar su estado.", "Error", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cambiar el estado: " + ex.getMessage(), "Error de BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

