    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int filaSElec = jTable1.getSelectedRow();

        if (filaSElec == -1) {
            JOptionPane.showMessageDialog(this, "Se tiene q elegir una instalacion de la tabla para eliminarla", "Requerido", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Object valorCLiCod = modeloTabla.getValueAt(filaSElec, 0);

        if (valorCLiCod == null) {
            JOptionPane.showMessageDialog(this, "No tiene codigo de instalacion valido esta fila", "Eror de datos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {

            int codInstal = (int) valorCLiCod;

            int confrimacion = JOptionPane.showConfirmDialog(this,
                "Seguro de eliminar la instalacion?", "Confirmar elimanar", JOptionPane.YES_NO_OPTION);

            if (confrimacion == JOptionPane.YES_OPTION) {

                instalacionData.borrarInstalacion(codInstal);
                JOptionPane.showMessageDialog(this,"Instalacion dada de baja","Dada de baja existosa", JOptionPane.INFORMATION_MESSAGE);

                Limpiar();
                actulizarTabla();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al dar de baja Detalle:" + e.getMessage(), "Error" , JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Instalacion nuevaInstalacion = obtenerInstalacion();

        if (nuevaInstalacion == null) {
            return;
        }

        int codigoIngresado = nuevaInstalacion.getCodInstal();

        try {

            if (instalacionData.existeInstalacion(codigoIngresado)){

                JOptionPane.showMessageDialog(this,
                    "️ El codigo de Instalacion " + codigoIngresado + " ya existe. No se puede guardar",
                    "Código Duplicado",
                    JOptionPane.WARNING_MESSAGE);

            } else {

                instalacionData.guardarInstalacion(nuevaInstalacion);

                JOptionPane.showMessageDialog(this,
                    "Instalacion: " + nuevaInstalacion.getNombre() + " guardada correctamente",
                    "Guardado Exitoso",
                    JOptionPane.INFORMATION_MESSAGE);

                //Limpiar();
                //actulizarTabla();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                " Error al intentar guardar el tratamiento: " + e.getMessage(),
                "Error al Guardar",
                JOptionPane.ERROR_MESSAGE);
        }
        
        Limpiar();
        actulizarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

