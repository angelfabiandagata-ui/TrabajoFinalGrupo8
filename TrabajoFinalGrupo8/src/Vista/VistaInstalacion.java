     private Instalacion obtenerInstalacion(){
 
    int codInstal = 0;
    double costo = 0;

    try {
        if (txtCodins.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "El Codigo es obligatorio", "Faltan Datos", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        
 
        codInstal = Integer.parseInt(txtCodins.getText().trim());


        if (!txtCostoins.getText().trim().isEmpty()) {
            costo = Double.parseDouble(txtCostoins.getText().trim());
        }

        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El codigo y el costo deben ser números validos", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        return null;
    }
    
    Instalacion nuevaInstalacion = new Instalacion();

    nuevaInstalacion.setCodInstal(codInstal);
    nuevaInstalacion.setNombre(txtNombreins.getText().trim());
    nuevaInstalacion.setDetalleUso(txtDetalleins.getText().trim());
    nuevaInstalacion.setPrecio30min(costo);
    boolean estadoActivo = chkEstadoins.isSelected();
    nuevaInstalacion.setEstado(estadoActivo);
    

    return nuevaInstalacion;
     }
     
     
private void configurarTabla(){
    String[] Titulos = {"codInstalacion", "Nombre", "detalle","costo","estado"};
    modeloTabla = new DefaultTableModel(Titulos, 0);
    jTable1.setModel(modeloTabla);
}

    
    private void Limpiar(){
        txtCodins.setText("");
        txtNombreins.setText("");
        txtDetalleins.setText("");
        txtCostoins.setText("");
        chkEstadoins.setSelected(false);
    }
    
     private void actulizarTabla(){
       
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel(); 
    modelo.setRowCount(0); 
    List<Instalacion> instalaciones = instalacionData.listarInstalaciones();
    System.out.println("Instalaciones cargadas de DB: " + instalaciones.size());
  
    for (Instalacion t : instalaciones) {

        modelo.addRow(new Object[]{
            t.getCodInstal(),
            t.getNombre(),
            t.getDetalleUso(),
            t.getPrecio30min(),
            t.isEstado()
        });
    }
}
     
      private void configurarSeleccionTabla() {

    jTable1.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            cargarCamposDesdeTabla();
        }
    });
}
    
    private void cargarCamposDesdeTabla() {
    int filaSeleccionada = jTable1.getSelectedRow();
    
    if (filaSeleccionada >= 0) {
        // Obtenemos el modelo de la tabla para acceder a los valores
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        
        // 1. Cargar el Código (Columna 0)
        String codInstal = modelo.getValueAt(filaSeleccionada, 0).toString();
        txtCodins.setText(codInstal);
        

        txtNombreins.setText(modelo.getValueAt(filaSeleccionada, 1).toString());
        txtDetalleins.setText(modelo.getValueAt(filaSeleccionada, 2).toString());
        
        // 3. Cargar Costo (Columna 5)
        txtCostoins.setText(modelo.getValueAt(filaSeleccionada, 3).toString());

        // 4. Cargar Estado (Columna 6)
        boolean estado = (Boolean) modelo.getValueAt(filaSeleccionada, 4);
        chkEstadoins.setSelected(estado);
        
        // 6. Deshabilitar la edición del código (ya que es la clave)
        txtCodins.setEnabled(false);
    }
}

